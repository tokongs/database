CREATE TABLE Company (
	CompanyID INTEGER NOT NULL AUTO_INCREMENT,
	URL	VARCHAR(45),
	Country	VARCHAR(45),
	Address	VARCHAR(45),
	PRIMARY KEY (CompanyID)
);

CREATE TABLE Media (
	MediaID INTEGER NOT NULL AUTO_INCREMENT,
	Title VARCHAR(45),
	Length INTEGER,
	PublicationYear	INTEGER,
	LaunchDate	DATETIME,
	Description	VARCHAR(512),
	CompanyID	INTEGER	NOT NULL,
	PRIMARY KEY (MediaID),
	FOREIGN KEY (CompanyID) REFERENCES Company(CompanyID)
);

CREATE TABLE Genre (
	GenreID	INTEGER	NOT NULL AUTO_INCREMENT,
	Name VARCHAR(45),
	PRIMARY KEY (GenreID)
);

CREATE TABLE MediaIsGenre (
	MediaID	INTEGER	NOT NULL,
	GenreID	INTEGER	NOT NULL,
	PRIMARY KEY (MediaID, GenreID),
	FOREIGN KEY (MediaID) REFERENCES Media(MediaID)
						ON UPDATE CASCADE
						ON DELETE CASCADE,
	FOREIGN KEY (GenreID) REFERENCES Genre(GenreID)
						ON UPDATE CASCADE
						ON DELETE CASCADE
);



CREATE TABLE Employee (
	EmployeeID INTEGER NOT NULL AUTO_INCREMENT,
	Name VARCHAR(45),
	BirthYear INTEGER,
	OriginCountry	VARCHAR(45),
	PRIMARY KEY (EmployeeID)
);

CREATE TABLE EmployeeActs (
	EmployeeID INTEGER NOT NULL,
