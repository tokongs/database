	PRIMARY KEY (EmployeeID, MediaID),
	FOREIGN KEY (EmployeeID) REFERENCES Employee(EmployeeID)
						ON UPDATE CASCADE
						ON DELETE CASCADE,
	FOREIGN KEY (MediaID) REFERENCES Media(MediaID)
						ON UPDATE CASCADE
						ON DELETE CASCADE
);

