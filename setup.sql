CREATE TABLE Series(
	SeriesID INTEGER NOT NULL AUTO_INCREMENT,
	Name VarChar(45),
	PRIMARY KEY (SeriesID)
);

CREATE TABLE Season(
	Number INTEGER NOT NULL,
	SeriesID INTEGER NOT NULL,
	PRIMARY KEY (Number, SeriesID),
	FOREIGN KEY (SeriesID) REFERENCES Series(SeriesID)
						ON UPDATE CASCADE
						ON DELETE CASCADE
);

CREATE TABLE SeasonHasEpisode(
	SeasonNumber INTEGER NOT NULL,
	Number INTEGER NOT NULL,
	SeriesID INTEGER NOT NULL,
	MediaID INTEGER NOT NULL,
	PRIMARY KEY(Number, SeasonNumber, SeriesID),
	FOREIGN KEY(MediaID) REFERENCES Media(MediaID)
						ON UPDATE CASCADE
						ON DELETE CASCADE
);

CREATE TABLE User(
	UserID INTEGER 	NOT NULL AUTO_INCREMENT,
	Username VARCHAR(45)	NOT NULL,
	PRIMARY KEY (UserID)
);

CREATE TABLE Review(
	ReviewID INTEGER NOT NULL AUTO_INCREMENT,
	Text VARCHAR(512) NOT NULL,
	Rating INTEGER	NOT NULL,
	UserID INTEGER	NOT NULL,
	MediaID INTEGER	NOT NULL,
	Number INTEGER	NOT NULL,
	SeriesID INTEGER NOT NULL,
	PRIMARY KEY (ReviewID),
	FOREIGN KEY (UserID) REFERENCES User(UserID)
						ON UPDATE CASCADE
						ON DELETE CASCADE,
	FOREIGN KEY (MediaID) REFERENCES Media(MediaID)
						ON UPDATE CASCADE
						ON DELETE CASCADE,
	FOREIGN KEY (Number, SeriesID) REFERENCES Season(Number, SeriesID)
						ON UPDATE CASCADE
						ON DELETE CASCADE,
	FOREIGN KEY (SeriesID) REFERENCES Series(SeriesID)
						ON UPDATE CASCADE
						ON DELETE CASCADE,
	CONSTRAINT Rating_Decimal CHECK (Rating >= 1 AND Rating <=10)
);